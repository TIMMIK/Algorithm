# Hash tables(en):
# Hash tables are a type of data structure in which the address or the index value of the data element is generated from a hash function. 
# That makes accessing the data faster as the index value behaves as a key for the data value.

# Hash tables(ru):
# Хэш-таблицы — это тип структуры данных, в которой адрес или значение индекса элемента данных генерируется с помощью хеш-функции.
# Это ускоряет доступ к данным, поскольку значение индекса ведет себя как ключ для значения данных.

# Hash function(en):
# Hash method in Python is a module that is used to return the hash value of an object. 
# In programming, the hash method is used to return integer values that are used to compare dictionary keys using a dictionary look up feature.

# Hash function(ru):
# Метод хеширования в Python — это модуль, который используется для возврата хеш-значения объекта.
# В программировании метод хеширования используется для возврата целочисленных значений, которые используются для сравнения ключей словаря с использованием функции поиска в словаре.

