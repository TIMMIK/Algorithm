# Dijkstra(en):
# Dijkstra's algorithm allows us to find the shortest path between any two vertices of a graph. 
# It differs from the minimum spanning tree because the shortest distance between two vertices might not include all the vertices of the graph.

# Dijkstra(ru):
# Алгоритм Дейкстры позволяет найти кратчайший путь между любыми двумя вершинами графа. 
# Он отличается от минимального остовного дерева тем, что кратчайшее расстояние между двумя вершинами может не включать все вершины графа.

# Big-O:
# N - Node
# E - Edge
# 1. For Breath-first: O(N + E)
# 2. For Dijkstra: O(N + E(logN))