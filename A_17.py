# Greedy algorithms[Жадные алгоритмы] (en):
# A greedy algorithm is a method of solving a problem that chooses the best solution available at the time. 
# It is not concerned about whether the current best outcome will lead to the overall best result.

# Greedy algorithms[Жадные алгоритмы] (ru):
# Жадный алгоритм — это метод решения проблемы, который выбирает наилучшее решение, доступное в данный момент.
# Его не волнует, приведет ли текущий лучший результат к общему лучшему результату.

# Set-covering problem(en):
# The set covering problem is a significant NP-hard problem in combinatorial optimization. 
# Given a collection of elements, the set covering problem aims to find the minimum number of sets that incorporate (cover) all of these elements.

# Set-covering problem(ru):
# Проблема покрытия множества является важной NP-трудной проблемой комбинаторной оптимизации. 
# Учитывая набор элементов, задача покрытия множества направлена ​​на поиск минимального количества наборов, которые включают (покрывают) все эти элементы.

# Big-O:
# For Greedy algorithms: O(n^2)[n во 2-ом степени]
# For Set-covering problem: O(2^n)[два в n-ной степени]
