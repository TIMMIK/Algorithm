# Dynamic programming(en):
# As you have understood, dynamic programming is the algorithm that divides the problem into various sub-problems to find out the optimal solution. 
# While solving the problem statement using the dynamic programming approach, the problem is divided into 3 elements in total to get the final result.

# Dynamic programming(ru):
# Как вы уже поняли, динамическое программирование — это алгоритм, который делит проблему на различные подзадачи, чтобы найти оптимальное решение.
# При решении постановки задачи с использованием подхода динамического программирования задача разбивается на 3 элемента всего для получения конечного результата.

# Knapsack Problem(en):
# A knapsack problem algorithm is a strategy for tackling combinatorial optimization constructively. 
# The problem is just a particular stack of objects, each having a specific weight and value.

# Knapsack Problem(ru):
# Алгоритм задачи о рюкзаке — это стратегия конструктивного решения комбинаторной оптимизации.
# Проблема заключается лишь в определенном наборе объектов, каждый из которых имеет определенный вес и ценность.

