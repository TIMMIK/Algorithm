# Classification(en):
# Classification is a prediction task with a categorical target variable.

# Classification(ru):
# Классификация — это задача прогнозирования с категориальной целевой переменной.

# Types of classification(en):
# There are two main types of classification:
# 1. Binary Classification – sorts data on the basis of discrete or non-continuous values (usually two values). 
# 2. Multi-class Classification – sorts data into three or more classes.

# Types of classification(ru):
# Существует два основных типа классификации:
# 1. Двоичная классификация — сортирует данные на основе дискретных или прерывистых значений (обычно двух значений).
# 2. Многоклассовая классификация — данные сортируются по трем или более классам.

# KNN(en):
# KNN Is a supervised learner for both classification and regression. 
# Supervised machine learning algorithms can be split into two groups based on the type of target variable that they can predict.

# KNN(ru):
# KNN Является контролируемым учеником как для классификации, так и для регрессии.
# Алгоритмы контролируемого машинного обучения можно разделить на две группы в зависимости от типа целевой переменной, которую они могут предсказать.

