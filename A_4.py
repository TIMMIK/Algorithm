# LL - Linked List(en):
# A linked list is a sequence of data elements, which are connected together via links. 
# Each data element contains a connection to another data element in the form of a pointer. 
# Python does not have linked lists in its standard library. 
# We implement the concept of linked lists using the concept of nodes as discussed in the previous chapter.

# LL - Linked List(ru):
# Связный список — это последовательность элементов данных, соединенных между собой ссылками. 
# Каждый элемент данных содержит связь с другим элементом данных в виде указателя. 
# В стандартной библиотеке Python нет связанных списков. 
# Мы реализуем концепцию связанных списков, используя концепцию узлов, как обсуждалось в предыдущей главе.

# SLL - Singly Linked List(en):
# In Singly Linked Lists each node points to only the next node. 
# On the other hand, in Doubly Linked Lists each node points to the next as well as to the previous node. 
# Manage memory more efficiently especially when the number of elements is unknown if arrays you may have to constantly shrink or grow them.

# SLL - Singly Linked List(ru):
# В односвязных списках каждый узел указывает только на следующий узел.
# С другой стороны, в двусвязных списках каждый узел указывает как на следующий, так и на предыдущий узел.
# Управляйте памятью более эффективно, особенно когда количество элементов неизвестно, если массивы вам, возможно, придется постоянно уменьшать или увеличивать.

# CLL - Circular Linked List(en):
# In a circular linked list, the head and the rear are adjacent to each other. 
# They are connected to form a circle, and don't have 'NULL' value in the last node. 
# Another 'linked_list' class needs to be created that would have an initialization function, and the head of the node would be initialized to 'None'.

# CLL - Circular Linked List(ru):
# В круговом связанном списке начало и конец расположены рядом друг с другом.
# Они соединены в круг и не имеют значения 'NULL' в последнем узле.
# Необходимо создать еще один класс 'linked_list', который будет иметь функцию инициализации, а заголовок узла будет инициализирован значением 'None'.

# DLL - Doubly Linked List(en):
# Doubly Linked List contains a link element called first and last. 
# Each link carries a data field(s) and two link fields called next and prev. 
# Each link is linked with its next link using its next link. 
# Each link is linked with its previous link using its previous link.

# DLL - Doubly Linked List(ru):
# Двусвязный список содержит элемент ссылки с именами first и last.
# Каждая ссылка содержит поле (поля) данных и два поля ссылки, называемые next и prev.
# Каждая ссылка связана со своей следующей ссылкой с помощью своей следующей ссылки.
# Каждая ссылка связана со своей предыдущей ссылкой, используя свою предыдущую ссылку.
